print('Usando operador de atribuicao +=   b += c: ' + str(b))
print('')
b -= c
print('Usabdo operador de atribuicao -=   b -= c: ' + str(b))
print('')
a *= c
print('Usando operador de atribuicao *=   a *= c: ' + str(a))
