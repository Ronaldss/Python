# Revisão - Operadores de atribuição

a = 10
b = 20
c = 30
bonos = 100

print('')
print("Valor original da variavel A: " + str(a))
print("Valor original da variavel B: " + str(b))
print("Valor original da variavel C: " + str(c))

print('')
resA = a + bonos
print('Variavel A + bonos: ' + str(resA))
print('')
